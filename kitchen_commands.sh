        init           #created a boiler plate .kitchen.yml
        list           # list all instances
        create         #create one or more instances 
        converge       #converge one or more instance 
        verify         #verify one or more instances
        destory        #destory one or more instances 
        test           #executes kitchen { destory, create, converge, verify, desotry} all in one
        login          #log in to one instance
        help           #provides a list of available kitchen commands
